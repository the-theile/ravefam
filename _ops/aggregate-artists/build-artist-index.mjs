#!/usr/bin/env node
// One-off aggregation script: parses every lineup-explorer/*.html file's
// hardcoded `const ACTS = [...]` array (same parsing approach as
// build-artists-seed.mjs) and emits a static artist -> festivals index
// consumed by lineup-explorer/index.html for cross-festival artist search.
//
// Run with: node build-artist-index.mjs
// Output: writes ../../lineup-explorer/artist-index.js. Re-run this whenever
// any lineup-explorer/*.html ACTS array changes so the index stays in sync.
// This script never talks to the DB.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const LINEUP_DIR = path.resolve(__dirname, '../../lineup-explorer');

const CANONICAL_NAME_OVERRIDES = {
  'illenium': 'ILLENIUM',
  'kettama': 'KETTAMA',
};

function extractBlock(text, startRe) {
  const m = startRe.exec(text);
  if (!m) return null;
  const openIdx = m.index + m[0].length - 1;
  const openChar = text[openIdx];
  const closeChar = openChar === '[' ? ']' : '}';
  let depth = 0;
  for (let i = openIdx; i < text.length; i++) {
    if (text[i] === openChar) depth++;
    else if (text[i] === closeChar) {
      depth--;
      if (depth === 0) return text.slice(openIdx + 1, i);
    }
  }
  return null;
}

function parseActs(actsBody) {
  const entries = [];
  const objRe = /\{([^{}]*)\}/g;
  let m;
  while ((m = objRe.exec(actsBody))) {
    const body = m[1];
    const nameM = /name:\s*"([^"]*)"/.exec(body);
    if (!nameM) continue;
    entries.push({ name: nameM[1].trim() });
  }
  return entries;
}

function splitB2B(name) {
  const parts = name.split(/\s+b2b\s+/i);
  return parts.length >= 2 ? parts.map(s => s.trim()) : [name];
}

function canonicalize(name) {
  const lower = name.toLowerCase();
  return CANONICAL_NAME_OVERRIDES[lower] || name;
}

function sqlStrEscapeForJs(s) {
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function extractTitle(text, slug) {
  const re = new RegExp(
    '"name":\\s*"([^"]+)",\\s*"item":\\s*"https:\\/\\/myravefam\\.com\\/lineup-explorer\\/' + slug + '"'
  );
  const m = re.exec(text);
  return m ? m[1] : slug;
}

const EXCLUDED_FILES = new Set(['index.html', 'og-image-source.html']);
const files = readdirSync(LINEUP_DIR).filter(f => f.endsWith('.html') && !EXCLUDED_FILES.has(f));

// name_lower -> { name, festivals: Map<slug, title> }
const artists = new Map();

for (const file of files) {
  const slug = file.replace(/\.html$/, '');
  const text = readFileSync(path.join(LINEUP_DIR, file), 'utf8');
  const title = extractTitle(text, slug);
  const actsBody = extractBlock(text, /const ACTS\s*=\s*(\[)/);
  if (!actsBody) {
    console.warn(`WARN: could not find ACTS block in ${file}`);
    continue;
  }
  const entries = parseActs(actsBody);

  for (const entry of entries) {
    const names = splitB2B(entry.name);
    for (const rawName of names) {
      const displayName = canonicalize(rawName);
      const key = displayName.toLowerCase();
      if (!artists.has(key)) {
        artists.set(key, { displayName, festivals: new Map() });
      }
      artists.get(key).festivals.set(slug, title);
    }
  }
}

const sortedArtists = [...artists.values()].sort((a, b) => a.displayName.localeCompare(b.displayName));

const lines = [];
lines.push('// Auto-generated by _ops/aggregate-artists/build-artist-index.mjs — do not hand-edit.');
lines.push('// Regenerate after editing any lineup-explorer/*.html ACTS array.');
lines.push('window.ARTIST_INDEX = [');
for (const a of sortedArtists) {
  const festivalsJs = [...a.festivals.entries()]
    .map(([slug, title]) => `{ slug: "${sqlStrEscapeForJs(slug)}", title: "${sqlStrEscapeForJs(title)}" }`)
    .join(', ');
  lines.push(`  { name: "${sqlStrEscapeForJs(a.displayName)}", name_lower: "${sqlStrEscapeForJs(a.displayName.toLowerCase())}", festivals: [${festivalsJs}] },`);
}
lines.push('];');
lines.push('');

const outPath = path.resolve(LINEUP_DIR, 'artist-index.js');
writeFileSync(outPath, lines.join('\n'));

console.log(`Parsed ${files.length} lineup-explorer files.`);
console.log(`Unique canonical artists: ${sortedArtists.length}`);
console.log(`Wrote ${outPath}`);
